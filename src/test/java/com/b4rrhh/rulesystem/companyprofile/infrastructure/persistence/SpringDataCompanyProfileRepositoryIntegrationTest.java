package com.b4rrhh.rulesystem.companyprofile.infrastructure.persistence;

import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.orm.jpa.DataJpaTest;
import org.springframework.dao.DataIntegrityViolationException;
import org.springframework.jdbc.core.JdbcTemplate;

import static org.junit.jupiter.api.Assertions.assertThrows;
import static org.junit.jupiter.api.Assertions.assertTrue;

@DataJpaTest(properties = {
        "spring.jpa.hibernate.ddl-auto=none",
        "spring.flyway.enabled=false"
})
class SpringDataCompanyProfileRepositoryIntegrationTest {

    @Autowired
    private SpringDataCompanyProfileRepository repository;

    @Autowired
    private JdbcTemplate jdbcTemplate;

    @BeforeEach
    void setUpSchema() {
        jdbcTemplate.execute("create schema if not exists rulesystem");
        jdbcTemplate.execute("drop table if exists rulesystem.company_profile");
        jdbcTemplate.execute("""
                create table rulesystem.company_profile (
                    id bigint generated by default as identity primary key,
                    company_rule_entity_id bigint not null,
                    legal_name varchar(200) not null,
                    tax_identifier varchar(50),
                    street varchar(300),
                    city varchar(120),
                    postal_code varchar(20),
                    region_code varchar(30),
                    country_code char(3),
                    created_at timestamp not null,
                    updated_at timestamp not null,
                    constraint uk_company_profile_company_rule_entity unique (company_rule_entity_id)
                )
                """);
    }

    @Test
    void enforcesUniqueConstraintPerCompanyRuleEntity() {
        repository.saveAndFlush(profileEntity(10L, "Acme Spain SA"));

        assertThrows(
                DataIntegrityViolationException.class,
                () -> repository.saveAndFlush(profileEntity(10L, "Another Legal Name"))
        );
    }

    @Test
    void findsByCompanyRuleEntityId() {
        repository.saveAndFlush(profileEntity(10L, "Acme Spain SA"));

        assertTrue(repository.findByCompanyRuleEntityId(10L).isPresent());
    }

    private CompanyProfileEntity profileEntity(Long companyRuleEntityId, String legalName) {
        CompanyProfileEntity entity = new CompanyProfileEntity();
        entity.setCompanyRuleEntityId(companyRuleEntityId);
        entity.setLegalName(legalName);
        return entity;
    }
}